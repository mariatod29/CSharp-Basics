using System;
class SymbolsToInt
{
    public void Main(String[] args)
    {
        char symbol = 'a';
        Console.WriteLine("The code of '" + symbol + "' is : " + (int)symbol);

        symbol = 'g';
        Console.WriteLine("The code of '" + symbol + "' is : " + (int)symbol);

        symbol = 'M';
        Console.WriteLine("The code of '" + symbol + "' is : " + (int)symbol);


    }
}